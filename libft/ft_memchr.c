#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	const char	*str;
	size_t		i;

	str = (const char *)s;
	i = -1;
	while (++i < n)
		if (*(str + i) == (char)c)
			return ((void *)str + i);
	return (NULL);
}
